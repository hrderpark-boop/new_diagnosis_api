"""진단 안내 등 표준 메시지 생성

LLM 이 가이드 텍스트를 학습 데이터 패턴으로 변형하는 문제를 방지하기 위해
표준 텍스트는 시스템이 직접 출력.
"""


def build_diagnosis_intro_message(user_name: str = "리더님") -> str:
    """진단 안내 메시지 생성.

    Args:
        user_name: 사용자 호칭 (기본값 "리더님")

    Returns:
        진단 안내 메시지
    """
    return (
        f"{user_name}, 진단에 대해 잠깐 안내드릴게요.\n\n"
        f"오늘 함께할 진단은 5개 영역의 리더십 진단이에요. "
        f"조직관리, 성과관리, 인재개발, 변화혁신, 자기개발 — "
        f"이렇게 5가지 영역을 차례로 살펴보게 됩니다.\n\n"
        f"영역마다 약 30분씩, 전체로는 2-3시간 정도 걸려요. "
        f"한 번에 다 하지 않으셔도 괜찮아요. 중간에 저장하고 "
        f"나중에 이어가실 수 있어요.\n\n"
        f"저와 리더님의 실제 경험을 바탕으로 편안하게 대화 "
        f"나누시면 돼요. 정답은 없으니 편하게 말씀해주시면 돼요.\n\n"
        f"이대로 시작해볼까요? 혹시 궁금하신 점 있으세요?"
    )
